package numPodC;

public class Endereco {
	public String rua;
	public String bairro;
	public String cidade;
	public int numero;
	
	public Endereco(String rua, String bairro, String cidade, int numero) {
		this.rua = rua;
		this.bairro = bairro;
		this.cidade = cidade;
		this.numero = numero;
	}
	
	
}
